            "vendor the upstream NEORV32 project and it does not program hardware by default.",
            "",
            "## Memory map",
            "",
            f"- ASCON CSR/MMIO base: `{memory['csr_base']}`",
            f"- AXI-stream MMIO bridge base: `{memory['axis_mmio_base']}`",
            "",
            "## Important toolchain note",
            "",
            "This target is mixed-language: the ASCON subsystem is Verilog and the NEORV32",
            "CFS wrapper is VHDL.  Plain Yosys/Gowin open-source flows usually need an",
            "upstream NEORV32 integration step or a VHDL-capable frontend before final",
            "synthesis.  This handoff therefore separates Verilog and VHDL sources and",
            "records tool availability instead of pretending a one-command bitstream flow",
            "exists today.",
            "",
            f"- Plain open-source flow ready: `{tool_notes['plain_open_source_flow_ready']}`",
            f"- Reason: {tool_notes['plain_open_source_flow_reason']}",
            "",
            "## Files",
            "",
            "- `handoff.json`: machine-readable handoff metadata",
