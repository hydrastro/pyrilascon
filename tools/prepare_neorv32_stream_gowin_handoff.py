#!/usr/bin/env python3
"""Prepare a Tang Nano 9K Gowin handoff for the NEORV32 stream-ASCON target.

This tool does not synthesize or program hardware.  It turns the validated board
package into an archivable handoff directory containing source lists, memory-map
metadata, firmware build helpers, and command templates for the first real
Tang Nano / NEORV32 integration attempt.
"""
from __future__ import annotations

import argparse
import json
import os
import shutil
import stat
from pathlib import Path
from typing import Any

from prepare_neorv32_stream_board_build import DEFAULT_OUT as DEFAULT_PACKAGE_DIR, prepare_package, validate_package

REPO_ROOT = Path(__file__).resolve().parents[1]
DEFAULT_OUT = REPO_ROOT / "build" / "neorv32_stream_axis_mmio" / "gowin_handoff"


class GowinHandoffError(RuntimeError):
    """Raised when the Gowin handoff cannot be generated or validated."""


def _read_json(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def _write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def _write_json(path: Path, payload: Any) -> None:
    _write_text(path, json.dumps(payload, indent=2, sort_keys=True) + "\n")


def _make_executable(path: Path) -> None:
    path.chmod(path.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)


def _read_file_list(path: Path) -> list[str]:
    return [line.strip() for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]


def _tool_status(name: str) -> dict[str, Any]:
    found = shutil.which(name)
    return {"available": found is not None, "path": found}


